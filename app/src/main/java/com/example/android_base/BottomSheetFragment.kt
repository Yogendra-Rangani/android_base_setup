package com.example.android_base

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.fragment.app.DialogFragment
import com.example.android_base.adapter.SearchableDialogFragment
import com.google.android.material.bottomsheet.BottomSheetDialogFragment

class BottomSheetFragment : BottomSheetDialogFragment() {

    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        return inflater.inflate(R.layout.bottom_sheet_layout, container, false)
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)

        // Show the searchable dialog inside the bottom sheet
        val searchableDialogFragment = SearchableDialogFragment()
        childFragmentManager.beginTransaction()
            .replace(R.id.dialogContainer, searchableDialogFragment)
            .commit()
    }
}
